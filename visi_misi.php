<h2>Visi dan Misi Perpustakaan XYZ</h2>

<h3>Visi</h3>
<p>Menjadi perpustakaan terdepan yang menginspirasi, mengedukasi, dan mendukung terciptanya masyarakat yang gemar membaca dan berpengetahuan luas.</p>

<h3>Misi</h3>
<ul>
    <li>Menyediakan koleksi bahan pustaka yang relevan dan mutakhir.</li>
    <li>Memberikan pelayanan yang ramah, cepat, dan berbasis teknologi informasi.</li>
    <li>Menciptakan lingkungan ruang baca yang nyaman dan kondusif bagi anggota.</li>
</ul>